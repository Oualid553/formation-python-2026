a = 2
b = 6
c = 3
resultat = str(a) + " + " + str(b) + " + " + str(c)

print(resultat)  # Affiche : 2 + 6 + 3

# Note : Les " + " sont des chaînes de caractères (texte)
#        Les + entre éléments sont des opérateurs de concaténation